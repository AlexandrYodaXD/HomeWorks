# Задайте число. Составьте список чисел Фибоначчи, в том числе для отрицательных индексов.
# Пример:
# для k = 8 список будет выглядеть так: [-21 ,13, -8, 5, −3, 2, −1, 1, 0, 1, 1, 2, 3, 5, 8, 13, 21] Негафибоначчи

num = 8
fib = [0, 1]

for _ in range(num):
    right = fib[-1] + fib[-2]
    fib.append(right)
    left = fib[1] - fib[0]
    fib.insert(0, left)
fib = fib[:-1]
print(fib)